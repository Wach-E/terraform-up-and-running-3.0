# Version module structure:
github.com/username/repo_name//path/to/a/module?ref=Git_commit_via:sha1_hash|brach_name|Git_tag(best)
Eg: 
- http: github.com/Wach-E/modules//services/webserver-cluster?ref=v0.0.1
- ssh: git@github.com:Wach-E/modules//services/webserver-cluster?ref=v0.0.1

After the Creating a new AWS account via organisation, use this link to switch the role:
```https://docs.aws.amazon.com/organizations/latest/userguide/orgs_manage_accounts_access.html#orgs_manage_accounts_access-as-root```


